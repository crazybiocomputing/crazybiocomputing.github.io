<h3>Etude du fonctionome de la flore intestinale d'un rat
Functional environmental screening</h3>



<p>Contexte</p>
<p>L'intestin humain héberge une communauté bactérienne complexe, comprenant à la fois des Firmicutes, Bacteroidete et Actinobacteria. La densité bactérienne augmente le long du tube digestif et ensemble, cette population bactérienne contient 150 fois plus de gènes que le génome de l'hôte. De plus, elle code pour de nombreuses fonctions métabolique qui font du microbiome de l'intestin un organe virtuel et fournit à l'hôte des fonctions metaboliques pouvant être absentes de son génome et lesquelles sont essentiel pour sa santé. </p>
<p>A partir d'un prélévement d'une flore intestinal d'un rat ayant été soumis à des radiations cosmiques d'une comète survolant la Terre par une nuit glaciale:
<p>Note: le  catabolisme du glucose  en pyruvate a été prouvé expérimentalement</p>
<p>Pour étudier les fonctions biochimiques présentes dans le métagénome, le séquençage du transcriptome a donné les séquences ci-dessous.
Quelle analyse proposez-vous pour étudier ces données ?
<ul>
<li>Etape 1 : Identifier les organismes d'origine de ces séquences et la fonction des protéines reliées à ces séquences.
<li>Etape 2 : Reconstruire les étapes de la voie métabolique qui permet de cataboliser le glucose en pyruvate (trouver les fonctions des protéines puis les relier aux résultats précédents). La base de données de Kegg permet de visualiser la carte métabolique de la glycolyse.
<li>Questions: 
<ul>
<li>Est-ce que l'hôte est autonome pour réaliser la glycolyse ?
<li>Est-ce que la voie métabolique de la glycolyse est complète ? 
<li>Existe t'il un CDS dont l'annotation est inconnue mais dont la fonction putative pourrait correspondre à l'enzyme manquante dans la voie ?
</ul>
</ul>


